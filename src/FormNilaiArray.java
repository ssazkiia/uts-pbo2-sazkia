    private void tampilkanData() {
        model.setRowCount(0);
        try {
            Connection c = config.Koneksi.getKoneksi();
            String sql;
            PreparedStatement p;

            if (txtNim.getText().equals("")) {
                // Tambahkan nama_mk di SELECT
                sql = "SELECT nim, nama_mk, uts, tugas, uas, absen, nilai_akhir FROM nilai";
                p = c.prepareStatement(sql);
            } else {
                sql = "SELECT nim, nama_mk, uts, tugas, uas, absen, nilai_akhir FROM nilai WHERE nim = ?";
                p = c.prepareStatement(sql);
                p.setString(1, txtNim.getText());
            }

            java.sql.ResultSet r = p.executeQuery();
            while (r.next()) {
                model.addRow(new Object[]{
                    r.getString("nim"),
                    r.getString("nama_mk"), // Ambil data mata kuliah dari database
                    r.getDouble("uts"),
                    r.getDouble("tugas"),
                    r.getDouble("uas"),
                    r.getDouble("absen"),
                    r.getString("nilai_akhir")
                });
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Gagal menampilkan data: " + e.getMessage());
        }
    }

