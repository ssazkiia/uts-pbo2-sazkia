    private void btnInputActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInputActionPerformed
        try {
            // 1. Ambil data dari inputan
            String nim = txtNim.getText();
            String matkul = cbMatkul.getSelectedItem().toString(); // Ambil matkul yang dipilih
            double uts = Double.parseDouble(txtUts.getText());
            double tugas = Double.parseDouble(txtTugas.getText());
            double uas = Double.parseDouble(txtUas.getText());
            double absen = Double.parseDouble(txtAbsen.getText());

            // Hitung Nilai Akhir sesuai format bapak dosen (30%, 20%, 40%, 10%)
            double na = (uts * 0.3) + (tugas * 0.2) + (uas * 0.4) + (absen * 0.1);

            // 3. Tentukan Grade (ini yang bakal jadi "Nilai Akhir" kamu)
            String grade;
            if (na >= 85) {
                grade = "A";
            } else if (na >= 75) {
                grade = "B";
            } else if (na >= 65) {
                grade = "C";
            } else if (na >= 50) {
                grade = "D";
            } else {
                grade = "E";
            }

            // 4. Simpan ke Database (Kolom nilai_akhir diisi huruf grade)
            Connection c = config.Koneksi.getKoneksi();
            String sql = "INSERT INTO nilai (nim, nama_mk, uts, tugas, uas, absen, nilai_akhir) VALUES (?, ?, ?, ?, ?, ?, ?)";
            PreparedStatement p = c.prepareStatement(sql);

            p.setString(1, nim);      // 1. nim (String)
            p.setString(2, matkul);   // 2. nama_mk (String)
            p.setDouble(3, uts);      // 3. uts (Double)
            p.setDouble(4, tugas);    // 4. tugas (Double)
            p.setDouble(5, uas);      // 5. uas (Double)
            p.setDouble(6, absen);    // 6. absen (Double) 
            p.setString(7, grade);    // 7. nilai_akhir (String/Grade A,B,C)

            p.executeUpdate();
            tampilkanData();

            // Bersihkan field setelah input (Opsional biar rapi)
            txtUts.setText("");
            txtTugas.setText("");
            txtUas.setText("");
            txtAbsen.setText("");

            // Notifikasi hanya sukses simpan saja, gak usah sebut Grade-nya
            JOptionPane.showMessageDialog(this, "Data Berhasil Disimpan!");

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Gagal simpan: " + e.getMessage());
        }
    }//GEN-LAST:event_btnInputActionPerformed

