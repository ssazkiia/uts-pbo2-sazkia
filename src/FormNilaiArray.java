    private void btnFindActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFindActionPerformed
        String nim = txtNim.getText();

        if (nim.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Masukkan NIM terlebih dahulu!");
            tampilkanData();
            return;
        }

        cbMatkul.removeAllItems(); // Bersihkan isi combo box sebelumnya

        try {
            Connection c = config.Koneksi.getKoneksi();
            // Query untuk mengambil nama matkul berdasarkan NIM melalui tabel KRS
            // Kita gunakan JOIN agar bisa dapat Nama Mata Kuliah-nya
            String sql = "SELECT m.nama_mk FROM krs k "
                    + "JOIN mata_kuliah m ON k.kode_mk = m.kode_mk "
                    + "WHERE k.nim = ?";

            PreparedStatement p = c.prepareStatement(sql);
            p.setString(1, nim);
            java.sql.ResultSet r = p.executeQuery();

            boolean ditemukan = false;
            while (r.next()) {
                cbMatkul.addItem(r.getString("nama_mk"));
                ditemukan = true;
            }

            if (!ditemukan) {
                JOptionPane.showMessageDialog(this, "NIM tidak ditemukan atau tidak mengontrak KRS!");
            } else {
                JOptionPane.showMessageDialog(this, "Data KRS ditemukan!");
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Gagal memuat KRS: " + e.getMessage());
        }
    }//GEN-LAST:event_btnFindActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        int barisTerpilih = tbNilai.getSelectedRow();

        if (barisTerpilih == -1) {
            JOptionPane.showMessageDialog(this, "Klik dulu baris di tabel yang mau dihapus!");
            return;
        }

        // Ambil NIM dari kolom ke-0 dan Mata Kuliah dari kolom ke-1
        String nimDihapus = tbNilai.getValueAt(barisTerpilih, 0).toString();
        String matkulDihapus = tbNilai.getValueAt(barisTerpilih, 1).toString();

        int confirm = JOptionPane.showConfirmDialog(this,
                "Hapus nilai " + matkulDihapus + " untuk NIM " + nimDihapus + "?",
                "Konfirmasi Hapus", JOptionPane.YES_NO_OPTION);

        if (confirm == JOptionPane.YES_OPTION) {
            try {
                Connection c = config.Koneksi.getKoneksi();

                // Hapus berdasarkan NIM DAN Mata Kuliah supaya spesifik 1 baris
                String sql = "DELETE FROM nilai WHERE nim = ? AND nama_mk = ?";
                PreparedStatement p = c.prepareStatement(sql);
                p.setString(1, nimDihapus);
                p.setString(2, matkulDihapus);

                int result = p.executeUpdate();

                if (result > 0) {
                    JOptionPane.showMessageDialog(this, "Data Berhasil Dihapus!");
                    tampilkanData();
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Gagal hapus: " + e.getMessage());
            }
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

