    private void btnFindActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFindActionPerformed
        String nim = txtNim.getText().trim();

        if (nim.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Masukan NIM");
            return;
        }

        try {
            Connection c = config.Koneksi.getKoneksi();

            // --- STEP 1: CARI NAMA MAHASISWA ---
            String sqlMhs = "SELECT nama FROM mahasiswa WHERE nim = ?";
            PreparedStatement pMhs = c.prepareStatement(sqlMhs);
            pMhs.setString(1, nim);
            ResultSet rMhs = pMhs.executeQuery();

            if (rMhs.next()) {
                txtNama.setText(rMhs.getString("nama"));

                // --- STEP 2: CARI DATA KRS (SEE DATA) ---
                // Kita langsung tampilkan apa saja yang sudah dia ambil di database
                DefaultTableModel model = (DefaultTableModel) tbKrs.getModel();
                model.setRowCount(0); // Bersihkan tabel UI dulu

                String sqlKrs = "SELECT id_krs, kode_mk, mata_kuliah, sks, dosen FROM krs WHERE nim = ?";
                PreparedStatement pKrs = c.prepareStatement(sqlKrs);
                pKrs.setString(1, nim);
                ResultSet rKrs = pKrs.executeQuery();

                boolean adaData = false;
                while (rKrs.next()) {
                    adaData = true;
                    model.addRow(new Object[]{
                        rKrs.getString("id_krs"), // Masukkan ID ke kolom 0
                        rKrs.getString("kode_mk"),
                        rKrs.getString("mata_kuliah"),
                        rKrs.getString("sks"),
                        rKrs.getString("dosen")
                    });
                }

                // Update total SKS di label
                updateLabelTotalSks();

                if (!adaData) {
                    JOptionPane.showMessageDialog(this, "Mahasiswa ditemukan, tapi belum mengambil KRS.");
                }

            } else {
                JOptionPane.showMessageDialog(this, "NIM '" + nim + "' tidak terdaftar di database!");
                txtNama.setText("");
                model.setRowCount(0);
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error Find: " + e.getMessage());
        }
    }//GEN-LAST:event_btnFindActionPerformed

