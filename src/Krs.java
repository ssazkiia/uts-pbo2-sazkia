    private void tampilkanDataKRS() {
        model.setRowCount(0);
        try {
            Connection c = config.Koneksi.getKoneksi();
            // Tetap tarik id_krs sebagai identitas unik
            String sql = "SELECT id_krs, kode_mk, mata_kuliah, sks, dosen FROM krs WHERE nim = ?";
            PreparedStatement p = c.prepareStatement(sql);
            p.setString(1, txtNim.getText());
            ResultSet r = p.executeQuery();

            while (r.next()) {
                model.addRow(new Object[]{
                    r.getString("id_krs"), // Indeks 0 (ID)
                    r.getString("kode_mk"), // Indeks 1
                    r.getString("mata_kuliah"), // Indeks 2
                    r.getString("sks"), // Indeks 3
                    r.getString("dosen") // Indeks 4
                });
            }
            updateLabelTotalSks();
        } catch (Exception e) {
            System.out.println(e.getMessage());
        }
    }

    private int hitungTotalSksDiTabel() {
        int total = 0;
        for (int i = 0; i < tbKrs.getRowCount(); i++) {
            total += Integer.parseInt(tbKrs.getValueAt(i, 2).toString());
        }
        return total;
    }

    // FUNGSI INI JUGA HARUS DI LUAR
    private void updateLabelTotalSks() {
        int total = hitungTotalSksDiTabel();
        totalSks.setText("Total SKS : " + total);
    }

    private void loadComboData() {
        try {
            Connection c = config.Koneksi.getKoneksi();

            // Load Mata Kuliah
            cbMatkul.removeAllItems();
            cbMatkul.addItem("-- Pilih Mata Kuliah --");
            ResultSet rMatkul = c.createStatement().executeQuery("SELECT nama_mk FROM mata_kuliah");
            while (rMatkul.next()) {
                cbMatkul.addItem(rMatkul.getString("nama_mk"));
            }

            // Load Dosen
            cbDosen.removeAllItems();
            cbDosen.addItem("-- Pilih Dosen --");
            ResultSet rDosen = c.createStatement().executeQuery("SELECT nama_dosen FROM dosen");
            while (rDosen.next()) {
                cbDosen.addItem(rDosen.getString("nama_dosen"));
            }

        } catch (Exception e) {
            System.out.println("Error Load Combo: " + e.getMessage());
        }
    }

