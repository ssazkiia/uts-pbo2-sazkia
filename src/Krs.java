    private void btnSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSubmitActionPerformed
        try {
            int sksBaru = Integer.parseInt(txtSks.getText());
            int totalSksSekarang = hitungTotalSksDiTabel();

            if (totalSksSekarang + sksBaru > 24) {
                JOptionPane.showMessageDialog(this, "Gagal! Total SKS melebihi batas maksimal 24 SKS.");
            } else {
                Connection c = config.Koneksi.getKoneksi();
                String sql = "SELECT kode_mk FROM mata_kuliah WHERE nama_mk = ?";
                PreparedStatement p = c.prepareStatement(sql);
                p.setString(1, cbMatkul.getSelectedItem().toString());
                ResultSet r = p.executeQuery();
                for (int i = 0; i < tbKrs.getRowCount(); i++) {
                    if (tbKrs.getValueAt(i, 1).toString().equals(cbMatkul.getSelectedItem().toString())) {
                        JOptionPane.showMessageDialog(this, "Mata kuliah ini sudah ada di daftar!");
                        return;
                    }
                }
                if (r.next()) {
                    model.addRow(new Object[]{
                        "",
                        r.getString("kode_mk"),
                        cbMatkul.getSelectedItem(),
                        txtSks.getText(),
                        cbDosen.getSelectedItem()
                    });
                    updateLabelTotalSks();
                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Pilih mata kuliah dulu!");
        }
    }//GEN-LAST:event_btnSubmitActionPerformed

    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        try {
            Connection c = config.Koneksi.getKoneksi();
            int rowsSaved = 0;

            for (int i = 0; i < tbKrs.getRowCount(); i++) {
                // Ambil ID untuk mengecek apakah data sudah ada di DB atau belum
                Object idObj = tbKrs.getValueAt(i, 0);

                // KITA HANYA INSERT DATA YANG ID-NYA MASIH KOSONG (DATA BARU)
                if (idObj == null || idObj.toString().trim().isEmpty()) {
                    String sql = "INSERT INTO krs (nim, kode_mk, mata_kuliah, sks, dosen) VALUES (?, ?, ?, ?, ?)";
                    PreparedStatement p = c.prepareStatement(sql);

                    p.setString(1, txtNim.getText());           // NIM dari TextField
                    p.setString(2, tbKrs.getValueAt(i, 1).toString()); // Kode MK (Sekarang di Indeks 1)
                    p.setString(3, tbKrs.getValueAt(i, 2).toString()); // Nama MK (Sekarang di Indeks 2)
                    p.setString(4, tbKrs.getValueAt(i, 3).toString()); // SKS (Sekarang di Indeks 3)
                    p.setString(5, tbKrs.getValueAt(i, 4).toString()); // Dosen (Sekarang di Indeks 4)

                    p.executeUpdate();
                    rowsSaved++;
                }
            }

            if (rowsSaved > 0) {
                JOptionPane.showMessageDialog(this, rowsSaved + " Mata kuliah baru berhasil disimpan!");
            } else {
                JOptionPane.showMessageDialog(this, "Tidak ada data baru untuk disimpan.");
            }

            // REFRESH DATA (Penting agar kolom ID yang tadi kosong jadi terisi dari DB)
            tampilkanDataKRS();

            // Kosongkan inputan matkul saja, NIM jangan dikosongkan agar user bisa lihat hasilnya
            txtSks.setText("");
            cbMatkul.setSelectedIndex(0);
            cbDosen.setSelectedIndex(0);

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Gagal Simpan: " + e.getMessage());
        }
    }//GEN-LAST:event_btnSaveActionPerformed

