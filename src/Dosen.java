    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        int confirm = javax.swing.JOptionPane.showConfirmDialog(this, 
            "Yakin ingin menghapus data " + txtKode.getText() + "?", 
            "Konfirmasi", javax.swing.JOptionPane.YES_NO_OPTION);
            
    if (confirm == javax.swing.JOptionPane.YES_OPTION) {
        try {
            java.sql.Connection c = config.Koneksi.getKoneksi();
            String sql = "DELETE FROM dosen WHERE kode_dosen = ?";
            java.sql.PreparedStatement p = c.prepareStatement(sql);
            p.setString(1, txtKode.getText());
            
            p.executeUpdate();
            p.close();
            
            javax.swing.JOptionPane.showMessageDialog(this, "Data Berhasil Dihapus");
            tampilkanData();
            kosongkanForm();
        } catch (java.sql.SQLException e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Gagal Hapus: " + e.getMessage());
        }
    }
    }//GEN-LAST:event_btnDeleteActionPerformed

