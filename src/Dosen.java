private void otomatisKode() {
    try {
        java.sql.Connection c = config.Koneksi.getKoneksi();
        java.sql.Statement s = c.createStatement();
        // Ambil kode_dosen terakhir
        String sql = "SELECT kode_dosen FROM dosen ORDER BY kode_dosen DESC LIMIT 1";
        java.sql.ResultSet r = s.executeQuery(sql);

        if (r.next()) {
            String lastKode = r.getString("kode_dosen");
            // Mengambil angka setelah huruf "D" (index ke-1 ke atas)
            int nomor = Integer.parseInt(lastKode.substring(1)) + 1;
            String kode = String.format("D%03d", nomor);
            txtKode.setText(kode);
        } else {
            // Jika tabel kosong
            txtKode.setText("D001");
        }
    } catch (Exception e) {
        txtKode.setText("D001");
    }
}
private void tampilkanData() {
    DefaultTableModel model = new DefaultTableModel();
    model.addColumn("Kode Dosen");
    model.addColumn("Nama Dosen");
    model.addColumn("Jabatan");

    try {
        java.sql.Connection c = config.Koneksi.getKoneksi();
        java.sql.Statement s = c.createStatement();
        java.sql.ResultSet r = s.executeQuery("SELECT * FROM dosen");

        while (r.next()) {
            model.addRow(new Object[]{
                r.getString("kode_dosen"),
                r.getString("nama_dosen"),
                r.getString("jabatan")
            });
        }
        tbDosen.setModel(model);
    } catch (java.sql.SQLException e) {
        javax.swing.JOptionPane.showMessageDialog(this, "Gagal Load Tabel: " + e.getMessage());
    }
}
private void kosongkanForm() {
    // 1. Generate kode dosen otomatis yang baru
    otomatisKode(); 
    
    // 2. Kosongkan field nama dosen
    txtDosen.setText("");
    
    // 3. Reset ComboBox ke pilihan pertama (index 0)
    cbJabatan.setSelectedIndex(0);
    
    // 4. Supaya kode bisa diisi lagi (jika sebelumnya false pas klik tabel)
    txtKode.setEditable(true);
    
    // 5. Fokuskan kursor ke Nama Dosen
    txtDosen.requestFocus(); 
}
public void isiDataDariFind(String kode, String nama, String jabatan) {
    txtKode.setText(kode);
    txtDosen.setText(nama);
    cbJabatan.setSelectedItem(jabatan);

    // Update tampilan tabel agar hanya menampilkan satu hasil yang dicari
    DefaultTableModel model = new DefaultTableModel();
    model.addColumn("Kode Dosen");
    model.addColumn("Nama Dosen");
    model.addColumn("Jabatan");
    model.addRow(new Object[]{kode, nama, jabatan});
    tbDosen.setModel(model);
}
