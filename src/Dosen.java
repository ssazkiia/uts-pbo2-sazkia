    private void btnInsertActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInsertActionPerformed
        try {
        java.sql.Connection c = config.Koneksi.getKoneksi();
        String sql = "INSERT INTO dosen (kode_dosen, nama_dosen, jabatan) VALUES (?, ?, ?)";
        java.sql.PreparedStatement p = c.prepareStatement(sql);
        
        p.setString(1, txtKode.getText());
        p.setString(2, txtDosen.getText());
        p.setString(3, cbJabatan.getSelectedItem().toString()); // Ambil data dari ComboBox
        
        p.executeUpdate();
        p.close();
        
        javax.swing.JOptionPane.showMessageDialog(this, "Data Dosen Berhasil Disimpan!");
        tampilkanData(); // Panggil method refresh tabel
        kosongkanForm(); // Panggil method reset input
    } catch (java.sql.SQLException e) {
        javax.swing.JOptionPane.showMessageDialog(this, "Gagal Simpan: " + e.getMessage());
    }
    }//GEN-LAST:event_btnInsertActionPerformed

