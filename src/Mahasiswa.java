    private boolean isValidEmail(String email) {
    // Pola standar email: teks + @ + teks + . + teks
    String emailRegex = "^[a-zA-Z0-9_+&*-]+(?:\\.[a-zA-Z0-9_+&*-]+)*@" + 
                        "(?:[a-zA-Z0-9-]+\\.)+[a-zA-Z]{2,7}$";
    
    java.util.regex.Pattern pat = java.util.regex.Pattern.compile(emailRegex);
    if (email == null)
        return false;
    return pat.matcher(email).matches();
}
    private void otomatisNIM() {
    try {
        java.sql.Connection c = config.Koneksi.getKoneksi();
        java.sql.Statement s = c.createStatement();
        // Ambil NIM terakhir untuk menentukan urutan berikutnya
        String sql = "SELECT nim FROM mahasiswa ORDER BY nim DESC LIMIT 1";
        java.sql.ResultSet r = s.executeQuery(sql);
        
        // Prefix 8 digit pertama (11 digit total - 3 digit urutan)
        String prefix = "24552011"; 

        if (r.next()) {
            String lastNim = r.getString("nim"); 
            
            // Logika: Ambil 3 angka paling belakang, lalu tambah 1
            // Jika NIM = 24552011001, substring(8) akan mengambil "001"
            int nomor = Integer.parseInt(lastNim.substring(8)) + 1;
            
            // Format kembali menjadi 11 digit
            String kode = prefix + String.format("%03d", nomor);
            txtNim.setText(kode);
        } else {
            // Jika tabel kosong, mulai dari urutan 001
            txtNim.setText(prefix + "001");
        }
    } catch (Exception e) {
        // Fallback jika terjadi error pada database atau parsing
        txtNim.setText("24552011001");
    }
}
