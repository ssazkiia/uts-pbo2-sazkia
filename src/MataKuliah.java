private void otomatisKode() {
    try {
        java.sql.Connection c = config.Koneksi.getKoneksi();
        java.sql.Statement s = c.createStatement();
        String sql = "SELECT kode_mk FROM mata_kuliah ORDER BY kode_mk DESC LIMIT 1";
        java.sql.ResultSet r = s.executeQuery(sql);

        if (r.next()) {
            String lastKode = r.getString("kode_mk");
            // Mengambil 3 angka terakhir setelah "MK"
            int nomor = Integer.parseInt(lastKode.substring(2)) + 1;
            String kode = String.format("MK%03d", nomor);
            txtKode.setText(kode);
        } else {
            txtKode.setText("MK001");
        }
    } catch (Exception e) {
        txtKode.setText("MK001");
    }
}
private void tampilkanData() {
    DefaultTableModel model = new DefaultTableModel();
    model.addColumn("Kode Mata Kuliah");
    model.addColumn("Nama Mata Kuliah");
    model.addColumn("SKS");

    try {
        java.sql.Connection c = config.Koneksi.getKoneksi();
        java.sql.Statement s = c.createStatement();
        java.sql.ResultSet r = s.executeQuery("SELECT * FROM mata_kuliah");

        while (r.next()) {
            model.addRow(new Object[]{
                r.getString("kode_mk"),
                r.getString("nama_mk"),
                r.getString("sks")
            });
        }
        tbMatkul.setModel(model);
    } catch (java.sql.SQLException e) {
        javax.swing.JOptionPane.showMessageDialog(this, "Gagal Load Tabel: " + e.getMessage());
    }
}
    private void kosongkanForm() {
    // 1. Panggil lagi fungsi otomatis agar kode MK bertambah (misal dari MK001 ke MK002)
    otomatisKode(); 
    
    // 2. Kosongkan field nama mata kuliah dan SKS
    txtMatkul.setText("");
    txtSks.setText("");
    
    // 3. Arahkan kursor langsung ke field Nama Matkul supaya user bisa langsung ngetik lagi
    txtMatkul.requestFocus(); 
}
    public void isiDataDariFind(String kode, String nama, String sks) {
    txtKode.setText(kode);
    txtMatkul.setText(nama);
    txtSks.setText(sks);

    // Update tampilan tabel agar hanya menampilkan hasil pencarian yang dipilih
    DefaultTableModel model = new DefaultTableModel();
    model.addColumn("Kode Mata Kuliah");
    model.addColumn("Nama Mata Kuliah");
    model.addColumn("SKS");
    model.addRow(new Object[]{kode, nama, sks});
    tbMatkul.setModel(model);
}
