    private void btnInsertActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInsertActionPerformed
    try {
        java.sql.Connection c = config.Koneksi.getKoneksi();
        String sql = "INSERT INTO mata_kuliah (kode_mk, nama_mk, sks) VALUES (?, ?, ?)";
        java.sql.PreparedStatement p = c.prepareStatement(sql);
        
        p.setString(1, txtKode.getText());
        p.setString(2, txtMatkul.getText());
        p.setString(3, txtSks.getText());
        
        p.executeUpdate();
        p.close();
        
        JOptionPane.showMessageDialog(this, "Data Mata Kuliah Berhasil Disimpan!");
        tampilkanData(); 
        kosongkanForm();
    } catch (java.sql.SQLException e) {
        JOptionPane.showMessageDialog(this, "Gagal Simpan: " + e.getMessage());
    }
    }//GEN-LAST:event_btnInsertActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        try {
        java.sql.Connection c = config.Koneksi.getKoneksi();
        // Query SQL untuk mengupdate data berdasarkan kode_mk
        String sql = "UPDATE mata_kuliah SET nama_mk = ?, sks = ? WHERE kode_mk = ?";
        java.sql.PreparedStatement p = c.prepareStatement(sql);
        
        p.setString(1, txtMatkul.getText());
        p.setString(2, txtSks.getText());
        p.setString(3, txtKode.getText()); // Primary Key sebagai acuan
        
        p.executeUpdate();
        p.close();
        
        JOptionPane.showMessageDialog(this, "Data Mata Kuliah Berhasil Diperbarui!");
        tampilkanData(); // Refresh tabel agar perubahan kelihatan
        kosongkanForm();
    } catch (java.sql.SQLException e) {
        JOptionPane.showMessageDialog(this, "Gagal Update: " + e.getMessage());
    }   
    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
    int opsi = JOptionPane.showConfirmDialog(this, "Yakin ingin menghapus data ini?", "Konfirmasi", JOptionPane.YES_NO_OPTION);
    
    if (opsi == JOptionPane.YES_OPTION) {
        try {
            java.sql.Connection c = config.Koneksi.getKoneksi();
            String sql = "DELETE FROM mata_kuliah WHERE kode_mk = ?";
            java.sql.PreparedStatement p = c.prepareStatement(sql);
            
            p.setString(1, txtKode.getText());
            
            p.executeUpdate();
            p.close();
            
            JOptionPane.showMessageDialog(this, "Data Mata Kuliah Telah Dihapus!");
            tampilkanData();
            kosongkanForm();
        } catch (java.sql.SQLException e) {
            JOptionPane.showMessageDialog(this, "Gagal Hapus: " + e.getMessage());
        }
    }
    }//GEN-LAST:event_btnDeleteActionPerformed

