    private void btnInsertActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInsertActionPerformed
        try {
        // 1. Ambil koneksi
        java.sql.Connection c = config.Koneksi.getKoneksi();
        
        // 2. Siapkan Query SQL
        String sql = "INSERT INTO users (id_user, username, password, level) VALUES (?, ?, ?, ?)";
        java.sql.PreparedStatement p = c.prepareStatement(sql);
        
        // 3. Isi parameter (?) dengan data dari form
        p.setString(1, txtId.getText()); 
        p.setString(2, txtUsername.getText());
        p.setString(3, new String(txtPassword.getPassword()));
        p.setString(4, cbLevel.getSelectedItem().toString());
        
        // 4. Eksekusi dan beri notifikasi
        p.executeUpdate();
        p.close();
        javax.swing.JOptionPane.showMessageDialog(null, "Penyimpanan Data Berhasil");
        
        // 5. Refresh tabel dan kosongkan form
        tampilkanData();
        kosongkanForm(); 
        
    } catch (java.sql.SQLException e) {
        javax.swing.JOptionPane.showMessageDialog(this, "Gagal Simpan: " + e.getMessage());
    }
    }//GEN-LAST:event_btnInsertActionPerformed

