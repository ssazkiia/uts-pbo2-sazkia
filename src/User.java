    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        try {
        Connection c = config.Koneksi.getKoneksi();
        String sql = "UPDATE users SET username=?, password=?, level=? WHERE id_user=?";
        PreparedStatement p = c.prepareStatement(sql);
        
        p.setString(1, txtUsername.getText());
        p.setString(2, new String(txtPassword.getPassword()));
        p.setString(3, cbLevel.getSelectedItem().toString());
        p.setString(4, txtId.getText());
        
        p.executeUpdate();
        p.close();
        JOptionPane.showMessageDialog(null, "Data Berhasil Diupdate");
        tampilkanData();
        kosongkanForm();
    } catch (SQLException e) {
        JOptionPane.showMessageDialog(this, "Gagal Update: " + e.getMessage());
    }
    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        int confirm = JOptionPane.showConfirmDialog(this, "Yakin ingin menghapus data ini?", "Konfirmasi Hapus", JOptionPane.YES_NO_OPTION);
        
        if (confirm == JOptionPane.YES_OPTION) {
            try {
                Connection c = config.Koneksi.getKoneksi();
                String sql = "DELETE FROM users WHERE id_user=?";
                PreparedStatement p = c.prepareStatement(sql);
                
                p.setString(1, txtId.getText());
                
                p.executeUpdate();
                p.close();
                
                JOptionPane.showMessageDialog(null, "Data Berhasil Dihapus");
                tampilkanData();
                kosongkanForm();
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(this, "Gagal Hapus: " + e.getMessage());
            }
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

